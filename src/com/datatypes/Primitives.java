package com.datatypes;

public class Primitives {
    public static void main(String[] args) {
        //These are primitive data-types
        int rollNo = 54;
        char letter = 'R';
        float marks = 98.67f;
        double largeDecimalNumbers = 456738.1256789;
        long largeIntegerValues = 65728038563792L;
        boolean is_tall = true;

        //String is not a primitive
        String name = "Utsab";
    }
}
